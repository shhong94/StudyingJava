package 자바제어문문제;

import java.util.*;
public class num_12 {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		System.out.print("정수 두개를 입력하세요 : ");
		int a = scan.nextInt();
		int b = scan.nextInt();
		
		System.out.println(a + " - " + b + " = " + (a - b));
		System.out.println(a + " * " + b + " = " + (a * b));

	}

}
